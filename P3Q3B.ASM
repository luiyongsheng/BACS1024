.MODEL SMALL
.STACK 64
.DATA
STR1 DB "Quantity$"
STR2 DB "Unit Price$"
STR3 DB "TOTAL AMOUNT$"
input DB ?
RESULT DB ?
Q DB ?
R DB ?
TEN DB 10
.CODE 
MAIN PROC
MOV AX,@DATA
MOV DS,AX

MOV AH,09H
LEA DX,STR1
INT 21H; OUTPUT STRING

MOV AH,01H
INT 21H;INPUT BYTE

MOV input,AL
SUB input,30H

MOV AH,02H
MOV DL,0DH
INT 21H
MOV DL,0AH
INT 21H;NEW LINE

MOV AX,0

MOV AH,09H
LEA DX,STR2
INT 21H

MOV AH,01H
INT 21H

SUB AL,30H

MUL input

DIV TEN

MOV Q,AL
MOV R,AH

ADD Q,30H
ADD R,30H


MOV AH,02H
MOV DL,0DH
INT 21H
MOV DL,0AH
INT 21H;NEW LINE

MOV AH,09H
LEA DX,STR3
INT 21H; OUTPUT STRING

MOV AH,02H
MOV DL,Q
INT 21H          ; OUPUT BYTE

MOV AH,02H
MOV DL,R
INT 21H          ; OUPUT BYTE





MOV AX,4C00H
INT 21H
MAIN ENDP
END MAIN
